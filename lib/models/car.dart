class Car {
  final String id;
  final String brand;
  final String model;
  final int year;
  final String plateNumber;
  final String vin;
  final String engineNumber;
  final int initialKm;

  const Car({
    required this.id,
    required this.brand,
    required this.model,
    required this.year,
    required this.plateNumber,
    required this.vin,
    required this.engineNumber,
    required this.initialKm,
  });
}

